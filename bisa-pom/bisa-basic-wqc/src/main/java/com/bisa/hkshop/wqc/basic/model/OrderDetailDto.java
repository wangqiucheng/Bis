package com.bisa.hkshop.wqc.basic.model;

public class OrderDetailDto {
	
	
	private String cartid;
	private String cartnum;
	private String cartkind;
	private String cartimg;
	private String cartdir;
	private String cartprice;
	
	
	public String getCartid() {
		return cartid;
	}
	public void setCartid(String cartid) {
		this.cartid = cartid;
	}
	public String getCartnum() {
		return cartnum;
	}
	public void setCartnum(String cartnum) {
		this.cartnum = cartnum;
	}
	public String getCartkind() {
		return cartkind;
	}
	public void setCartkind(String cartkind) {
		this.cartkind = cartkind;
	}
	public String getCartimg() {
		return cartimg;
	}
	public void setCartimg(String cartimg) {
		this.cartimg = cartimg;
	}
	public String getCartdir() {
		return cartdir;
	}
	public void setCartdir(String cartdir) {
		this.cartdir = cartdir;
	}
	public String getCartprice() {
		return cartprice;
	}
	public void setCartprice(String cartprice) {
		this.cartprice = cartprice;
	}
	
	
	
}
